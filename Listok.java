

public class Listok
{
    private String meno;
    private int vek;
    private int zostatokja;
    private int stlpec;
    private int riadok;
    
    public Listok(String meno, int vek, int zostatokja)
    {
        this.meno = meno;
        this.vek = vek;
        this.zostatokja = zostatokja;
        this.stlpec = stlpec;
        this.riadok = riadok;
    }
    
    public String getMeno() {
        return this.meno;
    }
    
    public int getVek() {
        return this.vek;
        
    }
    
    public int getZostatok() {
        return this.zostatokja;
    }
    
    


    
}
